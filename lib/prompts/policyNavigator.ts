// 추후 페르소나별 변주를 위한 빌더 함수 (확장성 대비)
export function buildSystemPrompt(opts?: {
  ageGroup?: 'youth' | 'middle' | 'senior';
  household?: 'single' | 'married' | 'parent';
}): string {
  let prompt = POLICY_NAVIGATOR_SYSTEM_PROMPT;
  
  if (opts?.ageGroup === 'senior') {
    prompt += '\n\n[추가 지침] 사용자가 노년층이므로 글자 크기 강조(굵게)를 더 자주 사용하고, 어려운 용어는 괄호로 풀어서 설명하세요.';
  }
  
  return prompt;
}
