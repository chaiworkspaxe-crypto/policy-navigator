// 🌟 시스템 프롬프트 빌더 — 시간 주입 + 페르소나 변주
// 항상 호출 시점의 최신 시간을 주입하도록 함수로만 제공합니다.
export function buildSystemPrompt(opts?: {
  ageGroup?: 'youth' | 'middle' | 'senior';
}): string {
  // Asia/Seoul 기준 "YYYY년 M월 D일 (요일)" 포맷
  const fmt = new Intl.DateTimeFormat('ko-KR', {
    timeZone: 'Asia/Seoul',
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    weekday: 'short',
  });

  const todayKr = fmt.format(new Date());

  // ISO 날짜도 같이 — 모델이 마감일 비교할 때 둘 다 활용
  const todayIso = new Intl.DateTimeFormat('en-CA', {
    timeZone: 'Asia/Seoul',
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
  }).format(new Date());

  let prompt = POLICY_NAVIGATOR_BASE_PROMPT.replace(/\{TODAY_KR\}/g, todayKr);

  prompt += `\n\n[오늘 날짜 — 신뢰할 수 있는 시스템 주입값]\n- 한국어: ${todayKr}\n- ISO: ${todayIso}\n- 위 값을 마감일 비교/안내문에 그대로 사용하세요. 도구 호출 불필요.\n`;

  if (opts?.ageGroup === 'senior') {
    prompt += '\n[추가 지침] 노년층이므로 굵게 강조를 자주 사용하고, 어려운 용어는 괄호로 풀어서 설명하세요.';
  }

  return prompt;
}

